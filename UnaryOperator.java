package Operators;

public class UnaryOperator {

	public static void main(String[] args) {
		int x = 10;
				int y=++x;
		int b = 10;
				int q=b++;
		int c = 10;
				int r=--c;
		int d = 10;
				int p=d--;
				System.out.println(y);
				System.out.println(q);
				System.out.println(r);
				System.out.println(p);
	}

}
